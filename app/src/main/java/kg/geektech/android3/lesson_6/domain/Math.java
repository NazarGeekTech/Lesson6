package kg.geektech.android3.lesson_6.domain;

public class Math {

    public int add(int a, int b) {
        return a + b;
    }
    public int minus(int a, int b) {
        return a - b;
    }

    public int multiplication(int a, int b) {
        return a * b;

    }
    public int division(int a, int b) {
        return a / b;

    }

}
